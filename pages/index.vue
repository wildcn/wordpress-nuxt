<template>
  <div class="content">
    <post-list v-model="posts" class="main"></post-list>
    <div class="secondary">
      <card></card>
    </div>
  </div>
</template>

<script>
  import PostList from '../components/PostList'
  import Card from '../components/Card'
  import $wp from '~/plugins/wpapi'
  import {
    PostModel,
    TagCollection,
    CategoryCollection,
    CommentCollection,
  } from '../resource'

  const tagCollection = TagCollection.getInstance()
  const categoryCollection = CategoryCollection.getInstance()
  const commentCollection = CommentCollection.getInstance()

  export default {
    async mounted(ctx) {
      try {
        const categoryMapList = await categoryCollection.fetchMap()
        const tagMapList = await tagCollection.fetchMap()
        const commentMapList = await commentCollection.fetchMap()

        // 获取列表

        const posts = await Promise.complete(
          []
            .concat(await $wp.posts(), await $wp.novel())
            .map(async (id) => await new PostModel(id))
        )
        // 获取标签
        this.posts = posts
        this.categoryMapList = categoryMapList
        this.tagMapList = tagMapList
        this.commentMapList = commentMapList

        return {
          posts,
          categoryMapList,
          tagMapList,
          commentMapList,
        }
      } catch (err) {
        console.log('Data -> err', err)
        return {
          posts: [],
          tagMapList: [],
          categoryMapList: [],
          commentMapList: [],
        }
      }
    },
    data() {
      return {
        posts: [],
        tagMapList: [],
        categoryMapList: [],
        commentMapList: [],
      }
    },
    components: {
      PostList,
      Card,
    },
    methods: {
      async fetchPosts(ctx) {
        try {
          const posts = await ctx.$wp.posts()
          const novel = await ctx.$wp.novel()
          const result = []
            .concat(posts, novel)
            .map((post) => new PostModel(post))
          return {
            posts: result,
          }
        } catch (err) {
          return {
            posts: [],
          }
        }
      },
    },
  }
</script>

